package dfsandbfs;

public class Node {
    /**
     * @Description: 节点值
     */
    public int value;
    /**
     * @Description: 左
     */
    public Node left;
    /**
     * @Description: 右
     */
    public Node right;

    public Node(){
        this.value = value;
        this.right = right;
        this.left = left;
    }
}
